package com.tyss.collection.linkedlist;

import java.util.LinkedList;

public class MyLinkedList {
       public static void main(String[] args) {
		
    	   LinkedList<Integer> ll= new LinkedList<Integer>();
    	   
    	   ll.add(10);
    	   ll.add(20);
    	   ll.add(200);
    	   ll.add(300);
    	   ll.addLast(50);
    	   ll.add(250);
    	   ll.add(100);
    	   ll.addFirst(40);
    	   
    	   
    	   System.out.println(ll);
    	   
    	   
	}
	
}
